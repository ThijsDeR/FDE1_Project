export default class Props {
private yPos: number
private xPos: number
private xVel: number
private yVel: number
private width: number
private height:number



public constructor() {
    this.xPos = this.xPos
    this.yPos = this.yPos
    this.yVel = 0;
    this.xVel = 0;
    this.width = this.width
    this.height = this.height

  }

  public getxPos() {
    return this.xPos
  }

  public getyPos() {
    return this.yPos
  }

  public getyVel() {
    return this.xVel
  }

  public getxVel() {
    return this.yVel
  }

  public getWidth() {
    return this.width
  }

  public getHeight() {
    return this.height
  }


}
