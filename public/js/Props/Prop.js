export default class Prop {
    constructor() {
        this.xPos = this.xPos;
        this.yPos = this.yPos;
        this.yVel = 0;
        this.xVel = 0;
        this.width = this.width;
        this.height = this.height;
    }
    getXPos() {
        return this.xPos;
    }
    getYPos() {
        return this.yPos;
    }
    getYVel() {
        return this.xVel;
    }
    getXVel() {
        return this.yVel;
    }
    getWidth() {
        return this.width;
    }
    getHeight() {
        return this.height;
    }
}
